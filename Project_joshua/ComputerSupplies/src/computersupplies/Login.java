    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String email = jTextFieldEmail.getText();
        String password = jPasswordField1.getText();
        
       try
        {  
            Class.forName("com.mysql.jdbc.Driver");  
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/CIS 2103","root","");
            PreparedStatement pst = con.prepareStatement("select * from users where email=? and password=?");
            pst.setString(1, email);
            pst.setString(2, password);
            ResultSet res = pst.executeQuery();
            
            if(res.next()){
                String s1 = res.getString("accType");
                if( s1.equalsIgnoreCase("admin")){
                    ConfirmOrder ord = new ConfirmOrder();
                    ord.setVisible(true);
                }else{
                    this.setVisible(false);
                    OrderList ad = new OrderList();
                    ad.setVisible(true);
            }
            }
        }
        catch(Exception e){ 
            System.out.println(e);
        }  
    }//GEN-LAST:event_jButton1MouseClicked

